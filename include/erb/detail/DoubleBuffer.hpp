namespace erb
{



/*\\\ PUBLIC \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : operator Buffer &
==============================================================================
*/

DoubleBuffer::operator Buffer & ()
{
   return _buffers [_cur_buf];
}



/*
==============================================================================
Name : operator const Buffer &
==============================================================================
*/

DoubleBuffer::operator const Buffer & () const
{
   return _buffers [_cur_buf];
}



/*
==============================================================================
Name : operator =
==============================================================================
*/

DoubleBuffer & DoubleBuffer::operator = (const Buffer & buffer)
{
   _buffers [_cur_buf] = buffer;

   return *this;
}



/*
==============================================================================
Name : size
==============================================================================
*/

std::size_t DoubleBuffer::size () const
{
   return _buffers [_cur_buf].size ();
}



/*
==============================================================================
Name : operator []
==============================================================================
*/

float &  DoubleBuffer::operator [] (std::size_t index)
{
   return _buffers [_cur_buf][index];
}


/*
==============================================================================
Name : operator []
==============================================================================
*/

const float &  DoubleBuffer::operator [] (std::size_t index) const
{
   return _buffers [_cur_buf][index];
}



/*
==============================================================================
Name : fill
==============================================================================
*/

void  DoubleBuffer::fill (float val)
{
   _buffers [_cur_buf].fill (val);
}



/*
==============================================================================
Name : tell
==============================================================================
*/

std::size_t DoubleBuffer::tell () const
{
   return _cur_index;
}



/*
==============================================================================
Name : push
==============================================================================
*/

void  DoubleBuffer::push (float spl)
{
   _buffers [1 - _cur_buf][_cur_index] = spl;
   ++_cur_index;

   if (_cur_index == erb_BUFFER_SIZE) swap ();
}



/*
==============================================================================
Name : pull
==============================================================================
*/

float DoubleBuffer::pull ()
{
   float spl = _buffers [1 - _cur_buf][_cur_index];
   ++_cur_index;

   if (_cur_index == erb_BUFFER_SIZE) swap ();

   return spl;
}



/*\\\ PROTECTED \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/



/*\\\ PRIVATE \\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\*/

/*
==============================================================================
Name : swap
==============================================================================
*/

void  DoubleBuffer::swap ()
{
   _cur_buf = 1 - _cur_buf;
   _cur_index = 0;
}



}  // namespace erb



